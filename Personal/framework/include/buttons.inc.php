<div class="col-24">
	<article>
		<h2>Basic setup</h2>
		<p>Buttons consist of either a button tag or anchor link tag. Classes are added to these tags for the various options listed below.</p>

		<script type="syntaxhighlighter" class="brush: xml"><![CDATA[

			<button class="ui-btn col-12 offset-4 medium orange first" id="" name="" value="" tabindex="" type="button" title="Sign up!">Sign up! (btn)</button>

		]]></script>

		<script type="syntaxhighlighter" class="brush: xml"><![CDATA[

			<a class="ui-btn col-12 offset-4 medium orange first" id="" href="#" name="" tabindex="" title="Sign up!">Sign up! (a)</a>

		]]></script>

		<p>For example: <em>class="ui-btn <strong>col-12 offset-4 medium orange</strong> first <strong>nomargin</strong>"</em>.</p>
		<p>The button options in order of class name (on the button or anchor tag) are:</p>
		<ol>
			
			<li>Width (not required)</li>
			<li>Offset (not required)</li>
			<li>Font size</li>
			<li>Color</li>
			<li>Margin left (required, 'first' class removes margin left on first button in a row of buttons)</li>
			<li>No margin (not required, removes margin-left on any button, regardless if it's first)</li>
		</ol>
	</article>
</div>

