			<h4>Dry-aged or wet?</h4>
			<p>
				During wet aging the plastic doesn't allow
				the meat to breathe, so it ages in contact
				with its own blood, which lends it "a more
				intense sour note and a more bloody serumy
				flavor."
			</p>
			<p>
				Dry aging, on the other hand allows the
				meat to breathe lose water (which increases
				its "beefiness" since there is now less water
				but the same amount of muscle fiber), and get
				acted upon by other microbes besides those of
				the muscle itself.
			</p>
