	<h1>Cooking guide</h1>
	<p>
		Flanagan Farms uses the highest quality beef and our flash freezing process locks in the taste and 
		texture of a hand-formed burger‚ without all the bother and mess! Just take what you need from your 
		box of Flanagan Farms and cook them up frozen.
	</p>
	<p>
		Remember that Flanagan Farms’ burgers go straight from the freezer to the Grill or Skillet.  Also 
		remember‚ DO NOT THAW OR REFREEZE Flanagan Farms’ burgers‚ as that will ruin their juicy flavor and 
		freshness.
	</p>

	<h4>Grill</h4>
	<ol>
		<li>Heat grill to about 325 degrees.</li>
		<li>Place frozen Flanagan Farms’ burger directly on grill rack.</li>
		<li>Don't flip the burger until the juices are clear.</li>
		<li>Once you flip the burger, cook it until it's  just how you like it.</li>
		<li>Serve on a bun with your favorite  condiments, or eat it alone like a steak.</li>
		<li>Repeat steps 1-5 as needed. </li>
	</ol>

	<h4>Skillet</h4>
	<ol>
		<li>Heat skillet or pan on medium heat at 325 degrees.</li>
		<li>Place frozen Flanagan Farms’ burger in pan.</li>
		<li>Don't flip the burger until the juices are clear.</li>
		<li>Once you flip the burger, cook it 'til it's just how you like it.</li>
		<li>Serve on a bun with you favorite condiments, or eat it alone like a steak.</li>
		<li>Repeat steps 1-5 as need</li>
	</ol>
