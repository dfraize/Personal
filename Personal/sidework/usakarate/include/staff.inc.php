			both in outside tournaments as well as the intramurals offered within the system.  Shihan Jon continues to be an influential role model in the assisted development 
			of the creation and improvement of effective new schools that represent the family values portrayed by the USA Karate institution.  Outside of the Martial Arts 
			Shihan Jon has held high ranking management positions in large industry corporations such as American Power Conversion where he was responsible for improving industry 
			quality and production standards for the corporations third shift operations.  Shihan Jon has completed an array of Management and Marketing courses at the University 
			of Rhode Island and continues to apply his experience within the Martial Arts for the benefit of everyone within the organization.
